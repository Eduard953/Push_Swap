#include "libft.h"

static int	ft_sign(char c)
{
	if (c == '+')
		return (1);
	else
		return (-1);
}

long	ft_atol(const char *nptr)
{
	int		i;
	int		j;
	long	nb;

	i = 0;
	nb = 0;
	j = 1;
	while (nptr[i] == '\t' || nptr[i] == '\n' || nptr[i] == '\v'
		|| nptr[i] == '\f' || nptr[i] == '\r' || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		j = ft_sign(nptr[i]);
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		nb = (nb * 10) + (nptr[i] - '0');
		i++;
	}
	return (nb * j);
}
